def book_page(book: dict, readme_html: str, readme_truncated: bool) -> str:
    """Render a single book page.

    `readme_html` is the book's README content rendered to HTML (may be empty).
    `readme_truncated` is True if the content was truncated to the word limit.
    """
    title = escape(book.get("title", "Untitled"))
    description = escape(book.get("description", ""))
    author = book.get("author")
    book_type = escape(book.get("book_type", "unknown"))
    form = escape(book.get("form", ""))
    media_type = escape(book.get("media_type", ""))
    section = escape(book.get("section", ""))
    date_added = escape(book.get("date_added", ""))
    status = book.get("status", "active")
    settled = bool(book.get("settled", False))
    slug = escape(book.get("slug", ""))
    path = book.get("path", "")

    # Metadata block
    metadata_rows = [
        f"<dt>Book type</dt><dd>{book_type}</dd>",
        f"<dt>Form</dt><dd>{form}</dd>",
    ]
    if media_type:
        metadata_rows.append(f"<dt>Media type</dt><dd>{media_type}</dd>")
    metadata_rows.append(f"<dt>Section</dt><dd><a href=\"../by-section/{section.lower().replace(' ', '-')}.html\">{section}</a></dd>")
    metadata_rows.append(f"<dt>Date added</dt><dd>{date_added}</dd>")
    if author:
        metadata_rows.insert(0, f"<dt>Author</dt><dd>{escape(author)}</dd>")

    if book.get("book_type") == "scout":
        if settled:
            settled_at = escape(book.get("settled_at", ""))
            metadata_rows.append(f"<dt>Scout status</dt><dd>Settled (static reference)</dd>")
            if settled_at:
                metadata_rows.append(f"<dt>Settled at</dt><dd>{settled_at}</dd>")
        else:
            metadata_rows.append(f"<dt>Scout status</dt><dd>Live (updates via discovery)</dd>")

    if status == "removed":
        removed_at = escape(book.get("removed_at", ""))
        removed_reason = escape(book.get("removed_reason", ""))
        metadata_rows.append(f"<dt>Status</dt><dd><strong>Removed</strong></dd>")
        if removed_at:
            metadata_rows.append(f"<dt>Removed at</dt><dd>{removed_at}</dd>")
        if removed_reason:
            metadata_rows.append(f"<dt>Removal reason</dt><dd>{removed_reason}</dd>")

    metadata_block = f"""<div class="book-metadata"><dl>
{''.join(metadata_rows)}
</dl></div>"""

    # Content block
    if status == "removed":
        content_block = """<div class="removed-notice">
<strong>Resource removed.</strong> This book is kept in the catalog as a historical
record. The original content is no longer in the library.
</div>"""
    elif book.get("book_type") == "scout" and not settled:
        # Live scout: thin catalog entry linking to the scout's own presentation
        content_block = f"""<div class="book-content">
<p><a href="../../{path}/README.md">Open the scout's full content</a> in a markdown viewer,
or <a href="../../{path}/">browse the scout directory</a>.</p>
</div>"""
    elif readme_html:
        truncation_note = ""
        if readme_truncated:
            truncation_note = f'<p><em>Content truncated. <a href="../../{path}/README.md">See full README</a>.</em></p>'
        content_block = f'<div class="book-content">\n{readme_html}\n{truncation_note}\n</div>'
    else:
        content_block = f"""<div class="book-content">
<p>Content is preserved in the book's directory. <a href="../../{path}/">Browse the book directory</a>.</p>
</div>"""

    description_html = f'<p class="description">{description}</p>' if description else ""

    body = f"""{description_html}

{metadata_block}

{content_block}

<p><a href="../index.html">← Back to library</a></p>
"""

    breadcrumb = f'<a href="../index.html">Home</a> / <a href="../by-section/{section.lower().replace(" ", "-")}.html">{section}</a> / {title}'
    return _page(title, breadcrumb, body, "../" + STYLESHEET_REL)
