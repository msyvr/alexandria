def collection_journal(library: dict, all_items: list[dict], entries: list[dict]) -> str:
    """Render the collection journal as a timeline page.

    Each entry is a dict with:
      - date: str (YYYY-MM-DD)
      - time: str (HH:MM)
      - accomplished: str (the headline)
      - full_html: str (the full checkpoint content rendered as HTML)
    """
    if not entries:
        body = """<p>No journal entries yet. After adding items or making changes to your collection,
run <code>/coll-notes</code> to record what was done. You can also add personal
notes about your collection — thoughts, plans, or anything you want to remember.</p>

<p>Journal entries appear here as a timeline, grouped by month.</p>
"""
        return _page("Journal", body, library, all_items, axes_current=None, from_subdir=True)

    # Group entries by year-month (newest first)
    months: dict[str, list[dict]] = {}
    for entry in entries:
        date = entry.get("date", "")
        if len(date) >= 7:
            month_key = date[:7]
        else:
            month_key = "undated"
        months.setdefault(month_key, []).append(entry)

    month_sections = []
    for month_key in sorted(months.keys(), reverse=True):
        month_entries = sorted(months[month_key], key=lambda e: e.get("date", "") + e.get("time", ""), reverse=True)

        entry_cards = []
        for entry in month_entries:
            date = escape(entry.get("date", ""))
            time = escape(entry.get("time", ""))
            accomplished = escape(entry.get("accomplished", ""))
            full_html = entry.get("full_html", "")

            timestamp = f"{date}"
            if time:
                timestamp += f" at {time}"

            headline = accomplished if accomplished else "Checkpoint"

            entry_cards.append(f"""<article class="journal-entry">
<div class="journal-date">{timestamp}</div>
<h3 class="journal-headline">{headline}</h3>
<div class="journal-detail">
{full_html}
</div>
</article>""")

        label = escape(month_key) if month_key != "undated" else "Undated"
        month_sections.append(
            f'<div class="section-group"><h2>{label}</h2>\n'
            + "\n".join(entry_cards)
            + "\n</div>"
        )

    body = f"""<p class="journal-intro">A timeline of your collection — what was added, changed, and decided,
in chronological order. Run <code>/coll-notes</code> to add entries with personal details.</p>

{''.join(month_sections)}
"""
    return _page("Journal", body, library, all_items, axes_current=None, from_subdir=True)


def search_page(library: dict, all_items: list[dict]) -> str:
    """Render the search page with inline JS for filtering and highlighting.

    Fully client-side: no external dependencies, no CDN, no network requests.
    The JS filters items to show only matches and highlights matching text.
    Case-insensitive partial matching. Works over file://.
    """
    # Build the flat listing — every active item with all searchable fields visible
    item_entries = []
    active = [b for b in all_items if b.get("status", "active") != "removed"]
    for item in sorted(active, key=lambda b: b.get("title", "").lower()):
        title = escape(item.get("title", "Untitled"))
        author = escape(item.get("author", ""))
        description = escape(item.get("description", ""))
        section = escape(item.get("section", ""))
        media_type = escape(item.get("media_type", ""))
        slug = item.get("slug", "")
        user_notes = escape(item.get("user_notes", ""))

        parts = [f'<div class="search-item" data-searchable>']
        parts.append(f'<div class="search-title"><a href="../items/{slug}.html">{title}</a></div>')
        if author:
            parts.append(f'<div class="search-meta">{author}</div>')
        if description:
            parts.append(f'<div class="search-desc">{description}</div>')
        detail_parts = []
        if section:
            detail_parts.append(section)
        if media_type:
            detail_parts.append(media_type)
        if detail_parts:
            parts.append(f'<div class="search-detail">{" · ".join(detail_parts)}</div>')
        if user_notes:
            parts.append(f'<div class="search-notes">{user_notes}</div>')
        parts.append('</div>')
        item_entries.append("\n".join(parts))

    listing = "\n".join(item_entries) if item_entries else "<p>No items in the collection yet.</p>"

    # The search script: ~80 lines inline, zero dependencies
    search_script = """
<script>
(function() {
  var input = document.getElementById('search-input');
  var prompt = document.getElementById('search-prompt');
  var items = document.querySelectorAll('[data-searchable]');
  var listing = document.querySelector('.search-listing');

  // Read query from URL on page load
  var params = new URLSearchParams(location.search);
  var initial = params.get('q') || '';
  if (initial) {
    input.value = initial;
    runSearch(initial);
  }

  // Live search on input (no need to press Enter after first load)
  input.addEventListener('input', function() {
    runSearch(input.value);
  });

  // Prevent form submission reload when typing — search live instead
  input.closest('form').addEventListener('submit', function(e) {
    e.preventDefault();
    runSearch(input.value);
    // Update URL without reload so bookmarking works
    var url = new URL(location);
    if (input.value) url.searchParams.set('q', input.value);
    else url.searchParams.delete('q');
    history.replaceState(null, '', url);
  });

  function runSearch(query) {
    var q = query.trim().toLowerCase();

    // Clear previous highlights
    items.forEach(function(el) {
      el.style.display = '';
      // Restore original text (strip <mark> tags)
      el.querySelectorAll('mark').forEach(function(m) {
        m.replaceWith(m.textContent);
      });
    });

    if (!q) {
      prompt.style.display = 'none';
      return;
    }

    var shown = 0;
    var terms = q.split(/\\s+/).filter(function(t) { return t.length > 0; });

    items.forEach(function(el) {
      var text = el.textContent.toLowerCase();
      var match = terms.every(function(t) { return text.indexOf(t) !== -1; });

      if (match) {
        el.style.display = '';
        shown++;
        // Highlight matching terms in text nodes
        highlightTerms(el, terms);
      } else {
        el.style.display = 'none';
      }
    });

    prompt.style.display = 'block';
    if (shown === 0) {
      prompt.innerHTML = 'No matches for <strong>' + esc(query) + '</strong>';
    } else {
      prompt.innerHTML = shown + ' match' + (shown === 1 ? '' : 'es') + ' for <strong>' + esc(query) + '</strong>';
    }
  }

  function highlightTerms(root, terms) {
    var walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, null);
    var nodes = [];
    while (walker.nextNode()) nodes.push(walker.currentNode);

    nodes.forEach(function(node) {
      var html = node.textContent;
      terms.forEach(function(term) {
        var re = new RegExp('(' + escRegex(term) + ')', 'gi');
        html = html.replace(re, '<mark>$1</mark>');
      });
      if (html !== node.textContent) {
        var span = document.createElement('span');
        span.innerHTML = html;
        node.replaceWith(span);
      }
    });
  }

  function esc(s) { return s.replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;'); }
  function escRegex(s) { return s.replace(/[.*+?^${}()|[\\]\\\\]/g, '\\\\$&'); }
})();
</script>"""

    body = f"""<form class="search-form" action="index.html" method="get">
<input type="text" name="q" placeholder="Search the collection…" id="search-input" autocomplete="off">
</form>

<div id="search-prompt" class="search-prompt" style="display:none;"></div>

<div class="search-listing">
{listing}
</div>

{search_script}
"""
    return _page("Search", body, library, all_items, axes_current=None, from_subdir=True)


def item_page(item: dict, readme_html: str, readme_truncated: bool, library: dict, all_items: list[dict], item_notes: list[dict] | None = None) -> str:
    """Render a single item page.

    `readme_html` is the item's README content rendered to HTML (may be empty).
    `readme_truncated` is True if the content was truncated to the word limit.
    `item_notes` is a list of note dicts from the notes/ directory, each with:
      filename, title, html, is_pdf.
    """
    title = escape(item.get("title", "Untitled"))
    description = escape(item.get("description", ""))
    author = item.get("author")
    user_notes = item.get("user_notes")
    book_type = escape(item.get("book_type", "unknown"))
    form = escape(item.get("form", ""))
    media_type = escape(item.get("media_type", ""))
    section = escape(item.get("section", ""))
    date_added = escape(item.get("date_added", ""))
    status = item.get("status", "active")
    settled = bool(item.get("settled", False))
    slug = escape(item.get("slug", ""))
    path = item.get("path", "")

    # Metadata grid
    metadata_rows = []
    if media_type:
        metadata_rows.append(f"<dt>Format</dt><dd>{media_type} ({form})</dd>")
    else:
        metadata_rows.append(f"<dt>Form</dt><dd>{form}</dd>")
    metadata_rows.append(f"<dt>Section</dt><dd><a href=\"../by-section/{section.lower().replace(' ', '-')}.html\">{section}</a></dd>")
    metadata_rows.append(f"<dt>Added</dt><dd>{date_added}</dd>")

    if item.get("book_type") == "scout":
        if settled:
            settled_at = escape(item.get("settled_at", ""))
            metadata_rows.append(f"<dt>Status</dt><dd>Settled{' ' + settled_at if settled_at else ''}</dd>")
        else:
            metadata_rows.append(f"<dt>Status</dt><dd>Live scout</dd>")

    if status == "removed":
        removed_at = escape(item.get("removed_at", ""))
        removed_reason = escape(item.get("removed_reason", ""))
        metadata_rows.append(f"<dt>Status</dt><dd><strong>Removed</strong>{' ' + removed_at if removed_at else ''}</dd>")
        if removed_reason:
            metadata_rows.append(f"<dt>Reason</dt><dd>{removed_reason}</dd>")

    metadata_block = f'<div class="item-metadata"><dl>{"".join(metadata_rows)}</dl></div>'

    # File link for digital items with original files
    file_link = ""
    if form == "digital" and status != "removed" and item.get("book_type") != "scout":
        # Check common original file extensions
        provenance = item.get("provenance", {}) if isinstance(item.get("provenance"), dict) else {}
        original_path_field = provenance.get("original_path", "")
        if original_path_field:
            file_link = f'<p class="file-link">Open <a href="../../{path}/{original_path_field}">{escape(original_path_field)}</a> from the <a href="../../{path}/">item directory</a></p>'
        else:
            file_link = f'<p class="file-link"><a href="../../{path}/">Browse item directory</a></p>'

    # Content block
    if status == "removed":
        content_block = """<div class="removed-notice">
<strong>Resource removed.</strong> This item is kept in the catalog as a historical
record. The original content is no longer in the collection.
</div>"""
    elif item.get("book_type") == "scout" and not settled:
        # Live scout: thin catalog entry linking to the scout's own presentation
        content_block = f"""<div class="item-content">
<p><a href="../../{path}/README.md">Open the scout's full content</a> in a markdown viewer,
or <a href="../../{path}/">browse the scout directory</a>.</p>
</div>"""
    elif readme_html:
        truncation_note = ""
        if readme_truncated:
            truncation_note = f'<p><em>Content truncated. <a href="../../{path}/README.md">See full README</a>.</em></p>'
        content_block = f'<div class="item-content">\n{readme_html}\n{truncation_note}\n</div>'
    else:
        content_block = f"""<div class="item-content">
<p>Content is preserved in the item's directory. <a href="../../{path}/">Browse the item directory</a>.</p>
</div>"""

    description_html = f'<p class="description">{description}</p>' if description else ""
    user_notes_html = f'<blockquote class="user-notes"><p>{escape(user_notes)}</p></blockquote>' if user_notes else ""

    # Render notes from notes/ directory
    notes_section = ""
    if item_notes:
        note_blocks = []
        for note in (item_notes or []):
            if note["is_pdf"]:
                note_blocks.append(
                    f'<div class="note-entry">'
                    f'<h3>{escape(note["title"])}</h3>'
                    f'<p>📄 <a href="../../{path}/notes/{note["filename"]}">Open as PDF</a></p>'
                    f'</div>'
                )
            else:
                note_blocks.append(
                    f'<div class="note-entry">'
                    f'<h3>{escape(note["title"])}</h3>'
                    f'{note["html"]}'
                    f'</div>'
                )
        notes_section = f"""<div class="item-notes">
<h2>Notes</h2>
{''.join(note_blocks)}
</div>"""

    metadata_link = ""
    if status != "removed" and path:
        metadata_link = (
            f'<p class="file-link">Open <a href="../../{path}/metadata.yaml">metadata.yaml</a> '
            f'from the <a href="../../{path}/">item directory</a></p>'
        )

    body = f"""{description_html}

{user_notes_html}

{metadata_block}

{file_link}

{content_block}

{notes_section}

{metadata_link}

<p><a href="../index.html">← Back</a></p>
"""

    return _page(title, body, library, all_items, axes_current=None, from_subdir=True)
